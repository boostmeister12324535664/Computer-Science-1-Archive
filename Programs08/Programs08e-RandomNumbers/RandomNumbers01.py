# RandomNumbers01.py
# This program uses the <randint> function from
# the <random> library 5 times to create 5 random 
# integers between 1 and 100.

# NOTE: When you execute the program a second time,
#       you will get a different set of numbers 
#       because they are "random".


# Required to have access to the <randint> command
from random import randint

print()
print(randint(1,100))
print(randint(1,100))
print(randint(1,100))
print(randint(1,100))
print(randint(1,100))
