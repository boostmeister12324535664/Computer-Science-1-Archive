# NumberFormat03.py
# This program demonstrates that leading 0s are
# not required to line up numbers by place value.
# Just leave out the 0, and the number will be
# displayed with leading spaces instead.


print()
print("{:5}".format(1))
print("{:5}".format(12))
print("{:5}".format(123))
print("{:5}".format(1234))
print("{:5}".format(12345))


