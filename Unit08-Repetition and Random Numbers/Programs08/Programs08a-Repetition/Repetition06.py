# Repetition06.py
# This program demonstrates how to make the <for>
# loop start counting at a number other than zero.
# The secret is to use 2 numbers in the <range> command.
# The counter will begin with the first number.
# and stop before it reaches the second number.


print()

for k in range(10,30): # Displays 10 to 29
   print(k, end = " ")
   
print("\n")

for k in range(10,31): # Displays 10 to 30
   print(k, end = " ")   
   
print()   
