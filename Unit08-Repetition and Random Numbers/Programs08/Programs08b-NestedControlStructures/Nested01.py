# Nested01.java
# This program demonstrates "Nested Repetition"
# which is one type of "Nested Control Structure".
# Since the outer loop repeats 3 times and the
# inner loop repeats 4 times, the word "Hello"  
# is displayed 3 * 4 or 12 times.


print()

for j in range(3):
   for k in range(4):
      print("Hello")
      